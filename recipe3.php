      <h4 class="text-muted">Ingredients</h4>
      <ul class="list-group ingg">
        <li>2½  cup cream cheese, room temperature</li>
        <li>1½ cup sugar</li>
        <li>1½  tablespoon cocoa powder</li>
        <li>5 tablespoon Godiva 85% Dark Chocolate Callets</li>
        <li>2 eggs</li>
        <li>2 egg yolks</li>
        <li>1½  tablespoon crème faiche</li>

      </ul>
      <br>
      <h4 class="text-muted">Recipe</h4>

      <ol style="1" class="steps">

        <li>In a food processor with a blade attachment, cream the cheese until smooth and then add in sugar. Pulse a couple times to incorporate sugar and cocoa powder. Add in eggs and yolks pulsing a couple times again and scrape down to ensure all is smooth and uniform. With machine running, pour in melted Godiva Chocolate in a steady stream until all in, and finish by pulsing in the crime fraiche. Pour batter into a 10" x 10" baking pan and bake at 325 for 30, minutes or until custard is set and a toothpick inserted in center can be removed cleanly. The top may caramelize or brown slightly, which is not an issue. Once set, remove from oven and allow cheesecake to cool completely. Wrap the pan with plastic and reserve in cooler to set and chill completely.
</li>
        <li>Once the cheesecake is cold and firm, remove it from the pan and place in a mixer bowl with a paddle attachment. Beat the cake for a few moments to soften and smooth out the custard. Place smoothed custard into a piping bag with a plain tip and pipe into the bottom of a plastic lined half cylinder mold. Tap the mold to ensure the custard is solid and then flatten out to top. Freeze the mold to harden the custard for unmolding. Once the cake can be unmolded, remove from the pan and cut into 1" sections. Brush some tempered Godiva Dark Chocolate onto a strip of acetate and, while it is still fluid, smooth over the top of the molded cheesecake. Once the chocolate has cooled and completely crystallized, remove the acetate, revealing a shiny and textured chocolate finish.

</li>



      </ol>


