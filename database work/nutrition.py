class Nutrition:
    def __init__(self):
        self.calories = 0
        self.fat = 0
        self.satfat = 0
        self.fiber = 0
        self.sugar = 0
        self.protein = 0

    def setCalories(self, x):
        self.calories = x

    def setFat(self, x):
        self.fat = x

    def setSatfat(self, x):
        self.satfat = x

    def setFiber(self, x):
        self.fiber = x

    def setSugar(self, x):
        self.sugar = x

    def setProtein(self, x):
        self.protein = x