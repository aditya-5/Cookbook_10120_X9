      <h4 class="text-muted">Ingredients</h4>
      <ul class="list-group ingg">
        <li>6 tablespoon lemon juice</li>
        <li>2 clove garlic, minced</li>
        <li>2 teaspoon kosher salt, plus more to taste</li>
        <li>1 cup extra-virgin olive oil, preferably Greek</li>
        <li>1 head romaine lettuce, trimmed of tough stems and torn into bite-sized pieces</li>
        <li>1 cup kalamata olives, about 6oz\/cup</li>
        <li>1½  pound Feta cheese, crumbled</li>
        <li>1 cucumber, trimmed, cut into 1 inch chunks</li>
        <li>1½  cup grape tomatoes, halved</li>
        <li>1 small red onion, cut into 1\/2-inch wedges, soaked in cold water for 5 minutes and drained</li>
        <li>2 teaspoon dried oregano</li>
        <li>Freshly ground black pepper</li>



      </ul>
      <br>
      <h4 class="text-muted">Recipe</h4>

      <ol style="1" class="steps">
        <li>In a small bowl, whisk together the lemon juice, garlic, salt, and oil. Set aside.   In a large bowl lightly toss the lettuce with the olives, cheese, cucumber, tomatoes, onion, oregano, and black pepper. Let sit up to 2 hours.  To serve, toss salad with dressing and place sliced chicken on top.</li>



      </ol>


